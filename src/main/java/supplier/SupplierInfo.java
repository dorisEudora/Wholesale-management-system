package supplier;

public class SupplierInfo {
	private int Id;
	private int Sno;
	private String Sname;
	private String Tel;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public int getSno() {
		return Sno;
	}
	public void setSno(int sno) {
		Sno = sno;
	}
	public String getSname() {
		return Sname;
	}
	public void setSname(String sname) {
		Sname = sname;
	}
	public String getTel() {
		return Tel;
	}
	public void setTel(String tel) {
		Tel = tel;
	}
}
